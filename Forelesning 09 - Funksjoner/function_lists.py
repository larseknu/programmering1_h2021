def print_list(list_to_print):
    for element in list_to_print:
        print(element)

print("Board games: ")
board_games = ['Ubongo', 'Pandemic', 'Ticket To Ride', 'Monopoly', 'Mysterium']
print_list(board_games)

print("\nRandom list: ")
random_list = [42, 9001, 3.14, 'Norway', 'Python']
print_list(random_list)
